        <h1>1.3 Trámites y requisitos de registro de obras ante la institución de derechos de autor
        </h1>
        <img id="img2" class="img" src="Images/p2img2.jpg" alt="Trámites y requisitos de registro de obras ante la institución de derechos de autor" />
        <p>Para registrar una obra ante la institución de derechos de autor en México, es necesario seguir una serie de trámites específicos:<br><br>

        <b>Solicitud de Registro:</b><br> 
        El primer paso es llenar el formato de solicitud correspondiente, que se puede obtener en la institución o en su sitio web.<br><br>

        <b>Presentación de la Obra:</b><br>
        Se debe entregar una copia de la obra que se desea registrar, ya sea en formato físico o digital, dependiendo del tipo de obra.<br><br>

        <b>Identificación:</b><br>
        Es necesario presentar una copia de identificación oficial del solicitante, como el INE o el pasaporte.<br><br>

        <b>Comprobante de Pago:</b><br>
        Se debe realizar el pago de la tarifa correspondiente y presentar el comprobante de pago junto con la solicitud.<br><br>

        <b>Declaración de Originalidad:</b><br>
        Es fundamental incluir una declaración firmada donde se manifieste que la obra es original y que el solicitante tiene el derecho a registrarla.<br><br>

        <b>Datos del Autor:</b><br>
        Debe proporcionar información sobre el autor o los coautores de la obra, incluyendo nombre, nacionalidad y datos de contacto.<br><br>

        Una vez que toda la documentación ha sido entregada, la institución revisará la solicitud. Si todo está en orden y es aprobada, se emitirá un certificado de registro que valida los derechos de autor sobre la obra. Este proceso asegura que los creadores tengan protección legal sobre sus obras y puedan ejercer sus derechos patrimoniales y morales.</p>
