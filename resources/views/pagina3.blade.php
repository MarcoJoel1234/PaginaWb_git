        <h1>1.5 Proceso de elaboración de avisos de privacidad</h1>
        <div class="text-img">
            <p>
            El aviso de privacidad es un documento fundamental que informa a los usuarios sobre el tratamiento que se le dará a sus datos personales. Este documento debe ser claro, accesible y cumplir con la normativa vigente en materia de protección de datos.<br>

            <b>Paso 1: Identificación de datos personales</b><br>
            El primer paso en la elaboración de un aviso de privacidad es identificar los tipos de datos personales que serán recopilados. Estos pueden incluir nombre, dirección, correo electrónico, número de teléfono, entre otros.<br></p>

            <img class="img" src="Images/p3img1.png" alt="Aviso de privacidad">
        </div>
            <h3>Paso 2: Propósito del tratamiento de datos</h3>
            <p>Es importante especificar el propósito para el cual se están recopilando los datos personales. Esto puede incluir finalidades como marketing, gestión de relaciones con clientes, cumplimiento de obligaciones legales, entre otros.</p><br>

            <h3>Paso 3: Consentimiento del usuario</h3>
            <p>Se debe informar al usuario sobre la necesidad de su consentimiento para el tratamiento de sus datos personales, y cómo puede otorgarlo o negarlo. También es necesario indicar cómo puede revocar su consentimiento en cualquier momento.</p><br>

            <h3>Paso 4: Derechos ARCO</h3>
            <p>El aviso de privacidad debe incluir información sobre los derechos ARCO (Acceso, Rectificación, Cancelación y Oposición) que tienen los usuarios respecto a sus datos personales, y cómo pueden ejercer estos derechos.</p><br>

            <img class="img" src="Images/p3img2.png" alt="Aviso de privacidad" style="padding:40px 0;">

            <h3>Paso 5: Transferencia de datos</h3>
            <p>Si los datos personales serán transferidos a terceros, es necesario informarlo claramente en el aviso de privacidad, indicando a quién se transferirán y con qué propósito.</p><br>

            <h3>Paso 6: Medidas de seguridad</h3>
            <p>Es fundamental describir las medidas de seguridad que se implementarán para proteger los datos personales contra acceso no autorizado, alteración, pérdida o destrucción.</p><br>

            <h3>Paso 7: Contacto</h3>
            <p>El aviso de privacidad debe proporcionar información de contacto para que los usuarios puedan realizar consultas o ejercer sus derechos en relación con sus datos personales.</p>
