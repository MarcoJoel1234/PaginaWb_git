        <h1>2.2 Características de los delitos informáticos tipificados en México</h1>
        <p>
            En México, los delitos informáticos están tipificados en la Ley Federal de Protección de Datos Personales en Posesión de los Particulares y en el Código Penal Federal. Algunos de los delitos informáticos más comunes en México incluyen:<br><br>

            <b>- Acceso no autorizado a sistemas informáticos:</b> Acceder a un sistema informático sin autorización para robar información o causar daño.<br>

            <b>- Fraude en línea:</b> Engañar a una persona para obtener información personal o financiera con el fin de cometer un fraude.<br>

            <b>- Robo de identidad:</b> Usar información personal de otra persona, como números de tarjeta de crédito o números de seguro social, para cometer fraudes.<br>

            <b>- Distribución de malware:</b> Crear y distribuir software malicioso con el fin de dañar sistemas informáticos o robar información.<br>

            <b>- Ciberacoso:</b> Acosar, amenazar o difamar a una persona a través de medios electrónicos, como redes sociales o correo electrónico.<br>

            <b>- Violación de la privacidad:</b> Obtener, divulgar o utilizar información personal de una persona sin su consentimiento.<br><br>

            La legislación mexicana establece penas para los delitos informáticos, que pueden incluir multas, prisión y reparación del daño. Además, las autoridades mexicanas trabajan en colaboración con otras agencias y países para investigar y prevenir estos delitos.
        </p>
